//It will keep executing as long as it is still true
//If the condition is always true, you will get infinite function run
var i = 1;

//5 is not less than 5 so it only run 4 times
while (i < 5) {
    document.write ("A statement has run");

    i++;
}

//Everything in 1 line for easy understanding
for (var i = 1; i <5; i++) {
    document.write ("A statement has run for");
}